class TrieNode:
    def __init__(self):
        self.children = dict()
        self.is_end = False

class Tire:
    def __init__(self):
        self.root = TrieNode()
    
    def insert(self,word):
        node = self.root 
        for c in word:
            if c not in node.children:
                node.children[c] = TrieNode()
            node = node.children[c]
        node.is_end = True

    def search(self,target):
        node = self.root
        prefix = ""

        for c in target:
            if c not in node.children:
                return target
            prefix += c 
            node = node.children[c]
            if node.is_end:
                return prefix
        return target   

class Solution(object):
    def replaceWords(self, dictionary, sentence):
        """
        Runtime: 263 ms, faster than 67.18% of Python online submissions for Replace Words.
        Memory Usage: 54.1 MB, less than 16.92% of Python online submissions for Replace Words.
        """
        tire = Tire()
        for word in dictionary:
            tire.insert(word)
        return ' '.join(tire.search(tar) for tar in sentence.split(' '))

